        <h1 class="pickus">Do You Need A Website?</h1><br>
        Probably. If you have a business, or want to start one, then absolutely. We live in a time where poeple don't go a day without looking at screens. People use laptops &amp; computers, snartphones &amp; tablets, and now even smart TVs to browse some sort of web service every single day, so it is important to have your presence online. WIth your own website, you have complete control of what your services are to your customers. <br><br>
        If you only have asocial media page, you will always be limited with what information you can provide. Having your own website (and email accounts) also looks more professional, plain and simple.
        <br><br><br>
   
        
        <h1 class="pickus">Work Ethic</h1><br>
        Each and every website we make starts from nothing. We don't use any templates, or any drag and drop programs. We write code. Some wbesites allow you to have drag and drop content on a blank page; and that's not a bad solution, but it's not the best. <br><br>Think about buying a pizza. A drag and drop website would be like buying a frozen pizza and putting it in a microwave for five minutes. It's easy, but not always great. Writing code is like baking a pizza using fresh ingredients; the timing is similar, but the outcome is much better. <br><br><br>
